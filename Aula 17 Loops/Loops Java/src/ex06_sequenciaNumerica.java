public class ex06_sequenciaNumerica {
    public static void main(String [] args) {
        for (int i = 1 ; i <= 200 ; i++) {
            System.out.println(i); // sequencia um abaico do outro
        }
        for (int i = 1 ; i <= 200 ; i++) {
            System.out.print(i + " ");  // sequencia um ao lado do outro
        }
    }
}